    @Audit(level = Audit.AuditLevel.FILE_ONLY)
    @Post(Paths.USER_ADMIN_REGISTER)
    CatalogueUser adminRegister(@Body @NonNull CatalogueUser newUser) {
        log.info 'Request to register a new user by admin'

        accessControlService.checkAdministrator()

        if (newUser.emailAddress == null || newUser.emailAddress.trim().isEmpty()) {
            log.debug('Missing email address')
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Missing email address')
        }

        if (newUser.firstName == null || newUser.firstName.trim().isEmpty()) {
            log.debug('Missing First name')
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Missing First name')
        }

        if (newUser.lastName == null || newUser.lastName.trim().isEmpty()) {
            log.debug('Missing Last name')
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Missing Last name')
        }

        if (!(newUser.emailAddress ==~ /^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$/)) {
            log.debug("Invalid email address syntax: ${newUser.emailAddress}")
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Invalid email address syntax')
        }

        if (catalogueUserRepository.existsByEmailAddress(newUser.emailAddress.trim())) {
            log.debug('The email address is already in use')
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'The email address is already in use')
        }

        newUser.pending = false
        newUser.disabled = false
        newUser.creationMethod = 'ADMIN_REGISTER'
        newUser.catalogueUser = accessControlService.user
        newUser.tempPassword = SecurityUtils.generateRandomPassword()
        newUser.salt = SecureRandomStringGenerator.generateSalt()
        newUser.password = null
        newUser.resetToken = null
        newUser.lastLogin = null
        newUser.profilePicture = null
        newUser.userPreferences = null

        CatalogueUser newCatalogueUser = catalogueUserRepository.save(newUser)

        Set<UserGroup> newGroups = newUser.groups
        newUser.groups = null

        if (newGroups) {
            newGroups.each {UserGroup userGroup ->
                userGroupRepository.addCatalogueUser(userGroup.id, newCatalogueUser.id)
            }
        }

        newCatalogueUser
    }

