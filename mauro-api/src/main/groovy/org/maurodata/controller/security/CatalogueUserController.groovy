    @Audit(level = Audit.AuditLevel.FILE_ONLY)
    @Put(Paths.USER_ID_CHANGE_PASSWORD)
    CatalogueUser changePassword(UUID id, @Body @NonNull ChangePassword changePasswordRequest) {

        accessControlService.checkAuthenticated()

        if (!accessControlService.administrator && accessControlService.userId != accessControlService.user.id) {
            throw new AuthorizationException(accessControlService.userAuthentication)
        }

        if (changePasswordRequest.oldPassword == null || changePasswordRequest.oldPassword.trim().isEmpty()) {
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Missing old password')
        }

        if (changePasswordRequest.newPassword == null || changePasswordRequest.newPassword.trim().isEmpty()) {
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Missing new password')
        }

        if (changePasswordRequest.oldPassword == changePasswordRequest.newPassword) {
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Old and new passwords must be different')
        }

        CatalogueUser existing = catalogueUserRepository.readById(id)

        if (existing.tempPassword != null) {
            if (existing.tempPassword != changePasswordRequest.oldPassword) {
                Thread.sleep(1000L)
                throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Old password is incorrect')
            }
        } else {
            byte[] hashedOldPassword = SecurityUtils.getHash(changePasswordRequest.oldPassword, existing.salt)
            byte[] actualOldPassword = existing.password
            if (hashedOldPassword != actualOldPassword) {
                Thread.sleep(1000L)
                throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Old password is incorrect')
            }
        }

        existing.salt = SecureRandomStringGenerator.generateSalt()
        existing.password = SecurityUtils.getHash(changePasswordRequest.newPassword, existing.salt)
        existing.tempPassword = null

        catalogueUserRepository.update(existing)

        return existing
    }

    @Audit(level = Audit.AuditLevel.FILE_ONLY)
    @Put(Paths.USER_ID)
    CatalogueUser update(@NonNull UUID id, @Body @NonNull CatalogueUser catalogueUser) {
        log.info 'Request to update CatalogueUser by ID'

        accessControlService.checkAuthenticated()

        Set<UserGroup> newGroups = catalogueUser.groups
        catalogueUser.groups = null
        catalogueUser.availableActions = null

        String emailAddress=catalogueUser.emailAddress

        cleanBody(catalogueUser, false)

        CatalogueUser existing = catalogueUserRepository.readById(id)

        if (!accessControlService.administrator && accessControlService.userId != existing.id) {
            throw new AuthorizationException(accessControlService.userAuthentication)
        }

        // Only an Administrator can add a user to Groups
        if (newGroups != null) {
            accessControlService.checkAdministrator()
        }

        boolean hasChanged = updateProperties(existing, catalogueUser)

        catalogueUser.emailAddress = emailAddress
        if (catalogueUser.emailAddress != null && existing.emailAddress != catalogueUser.emailAddress) {
            if (!(catalogueUser.emailAddress ==~ /^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$/)) {
                log.debug("Invalid email address syntax: ${catalogueUser.emailAddress}")
                throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'Invalid email address syntax')
            }
            hasChanged = true
            existing.emailAddress = catalogueUser.emailAddress
        }

        if (hasChanged) {
            catalogueUserRepository.update(existing)
        }

        if (newGroups != null) {
            List<UUID> existingUserGroupIds = userGroupRepository.readAllByCatalogueUserId(existing.id).id
            List<UUID> newUserGroupIds = newGroups.collect {it.id}.findAll {it !in existingUserGroupIds}
            newUserGroupIds.each {UUID userGroupId ->
                userGroupRepository.addCatalogueUser(userGroupId, existing.id)
            }

            List<UUID> allNewUserGroupIds = newGroups.collect {it.id}
            List<UUID> removedUserGroupIds = existingUserGroupIds.findAll {it !in allNewUserGroupIds}
            removedUserGroupIds.each {UUID userGroupId ->
                userGroupRepository.deleteCatalogueUser(userGroupId, existing.id)
            }
        }

        existing
    }

    @Audit
    @Get(Paths.USER_PREFERENCES)
    Map showUserPreferences(UUID id) {
        accessControlService.checkAuthenticated()
        CatalogueUser existing = catalogueUserRepository.readById(id)

        if (!accessControlService.administrator && accessControlService.userId != existing.id) {
            throw new AuthorizationException(accessControlService.userAuthentication)
        }

        String userPreferences = existing.userPreferences

        if (userPreferences == null || userPreferences.trim().isEmpty()) {
            return [:]
        }

        JsonSlurper jsonSlurper = new JsonSlurper()
        try {
            Object userPreferencesObject = jsonSlurper.parseText(userPreferences)
            if (!userPreferencesObject instanceof Map) {
                return [:]
            }
            return (Map) userPreferencesObject
        }
        catch (Throwable ignored) {
            return [:]
        }
    }

    @Audit
    @Put(Paths.USER_PREFERENCES)
    CatalogueUser updateUserPreferences(@NonNull UUID id, @Body @NonNull String userPreferencesBody) {
        accessControlService.checkAuthenticated()

        CatalogueUser existing = catalogueUserRepository.readById(id)

        if (!accessControlService.administrator && accessControlService.userId != existing.id) {
            throw new AuthorizationException(accessControlService.userAuthentication)
        }

        JsonSlurper jsonSlurper = new JsonSlurper()
        try {
            jsonSlurper.parseText(userPreferencesBody)
            existing.userPreferences = userPreferencesBody
            catalogueUserRepository.update(existing)
        }
        catch (Throwable ignored) {
        }

        return existing
    }

