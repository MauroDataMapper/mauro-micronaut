        }

        return ListResponse.from(all, params)
    }

    @Audit
    @Put(Paths.USER_ADMIN_PASSWORD_RESET)
    CatalogueUser adminPasswordReset(UUID id){
        if (!accessControlService.administrator) {
            throw new AuthorizationException(accessControlService.userAuthentication)
        }
        CatalogueUser existing = catalogueUserRepository.readById(id)

        if(existing==null){
            log.debug("User with id ${id} not found")
            throw new HttpStatusException(HttpStatus.UNPROCESSABLE_ENTITY, 'User with id ${id} not found')
        }

        log.debug("Resetting user '${existing.emailAddress}' password(Actor: '${accessControlService.user.emailAddress}')")

        existing.tempPassword = SecurityUtils.generateRandomPassword()
        existing.salt = SecureRandomStringGenerator.generateSalt()
        existing.password = null
        existing.resetToken = null

        catalogueUserRepository.update(existing)

        final String SITE_URL = ApiProperty.ApiPropertyEnum.SITE_URL.key
        final String EMAIL_PASSWORD_RESET_SUBJECT = ApiProperty.ApiPropertyEnum.EMAIL_PASSWORD_RESET_SUBJECT.key
        final String EMAIL_PASSWORD_RESET_BODY = ApiProperty.ApiPropertyEnum.EMAIL_PASSWORD_RESET_BODY.key
        final String EMAIL_FROM_NAME = ApiProperty.ApiPropertyEnum.EMAIL_FROM_NAME.key
        final String EMAIL_FROM_ADDRESS = ApiProperty.ApiPropertyEnum.EMAIL_FROM_ADDRESS.key

        ApiProperty siteUrlProperty = apiPropertyCacheableRepository.findByKey(SITE_URL)
        ApiProperty emailPasswordResetSubjectProperty = apiPropertyCacheableRepository.findByKey(EMAIL_PASSWORD_RESET_SUBJECT)
        ApiProperty emailPasswordResetBodyProperty = apiPropertyCacheableRepository.findByKey(EMAIL_PASSWORD_RESET_BODY)
        // ApiProperty emailFromNameProperty = apiPropertyCacheableRepository.findByKey(EMAIL_FROM_NAME)
        // ApiProperty emailFromAddressProperty = apiPropertyCacheableRepository.findByKey(EMAIL_FROM_ADDRESS)

        final Map<String,String> userPropertiesMap = [

            catalogueUrl: siteUrlProperty?.value?:"",
            firstName   : existing.firstName?:"",
            lastName    : existing.lastName?:"",
            emailAddress: existing.emailAddress?:"",
            tempPassword: existing.tempPassword?:""
        ]

        // TODO: Missing From address in Email e.g. fullName <email address>

        Email email = new Email().tap {
            subject = substituteInMap(emailPasswordResetSubjectProperty?.value ,userPropertiesMap)
            body = substituteInMap(emailPasswordResetBodyProperty?.value ,userPropertiesMap)
        }

        log.debug(email.toString())

        emailService.sendEmail(existing,email)

        existing
    }

    private static String substituteInMap(final String inValue, final Map<String,String> withPropertiesMap) {
        if(inValue == null) {return null}

        StringSubstitutor stringSubstitutor = new StringSubstitutor(withPropertiesMap)
        return stringSubstitutor.replace(inValue)
    }

